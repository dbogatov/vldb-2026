    <h1>VLDB 2025: Call for Contributions - Tutorials</h1>

    <p>VLDB 2025 invites submissions for tutorial proposals on all topics of potential interest to the conference attendees. Tutorial proposals should cover state-of-the-art research, development, and applications in specific data management related areas, and stimulate and facilitate future work. Tutorials on interdisciplinary directions, bridging scientific research and applied communities, novel and fast-growing directions, and significant applications are highly encouraged. We encourage tutorials in areas that may be different from the usual VLDB mainstream, but still very much related to VLDB mission and objectives of managing big data. We also encourage tutorials that apply advanced Machine Learning techniques to solve data management problems, and will be directly inviting a small number of tutorials from neighboring scientific communities. Tutorials should be targeted for a broad audience, and they must focus either excessively, nor exclusively, on the authors’ own work.</p>

    VLDB 2025 welcomes two types of tutorials. Lecture-style tutorials will be typically 1.5 hours in duration, while hands-on tutorials can be either 1.5 hours or 3 hours long.

     <ul>
        <li>A lecture-style tutorial (L) will cover the state-of-the-art research, development, and applications in a specific data management or related area, and stimulate and facilitate future work. Tutorials on interdisciplinary directions, bridging scientific research and applied communities, novel and fast growing directions, and significant applications are highly encouraged. </li>
          <li>A hands-on tutorial (H) will feature in-depth hands-on training on cutting-edge systems and tools of relevance to the data management community. These sessions are targeted at novice as well as moderately skilled users. The focus should be on providing hands-on experience to the attendees. Tutorials should introduce the motivation behind the tool, associated fundamental concepts, and work through examples, and demonstrate its application to relatable real-life use cases. The pace of the tutorial should be adequate for beginners, e.g., early-stage Ph.D. and master students</li>
    </ul>

    <h3>Important Dates (All deadlines below are 5pm PT.)</h3>
    <ul>
        <li>Submission deadline: <strike>April 7, 2025</strike> <strike>April 14, 2025</strike> April 18, 2025</li>
        <li>Notification: May 19, 2025</li>
        <li>Camera-ready abstract overview due: June 23, 2025</li>
        <li>Slides availability: August 16, 2025</li>
    </ul>

    <h3>Submission Guidelines</h3>
    <p>Tutorial submissions must be submitted electronically using <a href=" https://cmt3.research.microsoft.com/PVLDBv16_2025">CMT</a>. Submissions should be formatted using the <a href="./?formatting-guidelines">PVLDB style templates</a>, with a maximum length of 4 pages, inclusive of ALL material.</p>
    <p>Proposals should include:</p>
    <ul>
        <li>Title of the tutorial</li>
        <li>Names, affiliations and email addresses of the presenters</li>
        <li>Overview of tutorial, with justification of its relevance and timeliness</li>
        <li>Target audience and assumed background</li>
        <li>Scope and structure: enough detail to provide a sense of both the scope of material to be covered and the depth to which it will be covered</li>
        <li>Intended length of the tutorial (one session of 1.5 hours, or two sessions with a total of 3 hours on the same day). If the tutorial can be of either length, please identify which sections are included for each option. Please also justify that a high-quality learning experience will be achieved within the chosen time period.</li>

        <li>Please indicate whether this will be a lecture-style or hands-on tutorial. In the case of the latter, please indicate the equipment needs for participants (e.g., pre-installed Jupyter notebook with specific packages). </li>

        <li>References: include at least 10 primary and relevant bibliographic references on the core material of the tutorial.</li>
        <li>Brief professional biographies of presenters, with a note on their background in the area of the tutorial</li>
        <li>Identification of any other venues in which all or part of the tutorial has been or will be presented, with an explanation of how the tutorial proposal for VLDB 2025 differs from these previous or forthcoming editions.</li>
        <li>Identification of topically related tutorials by other people in major data-centric research venues in recent years.</li>
    </ul>

    <h3>Accepted Tutorials</h3>
    <p>Presentation slides will be made available to VLDB 2025 participants and will be published on the VLDB website. We require the tutorial presenters to put online a webpage that provides links to all reasonably drafted slides and references before the conference starts, for publication at the conference website; an example can be found <a href="https://shangjingbo1226.github.io/2017-08-11-kdd-tutorial/">here</a>.</p>
    <p>A four-page extended abstract, with an overview including bibliographic references, will appear in PVLDB's VLDB 2025 issue. The <a href="./?formatting-guidelines">camera-ready format</a> version should be an updated version of the original submission that addresses suggestions from the tutorial chairs and possibly other reviewers.</p>
    <p>Authors must agree to standard PVLDB copyright releases for the tutorial abstract overview as well as for their slides. Authors will retain the rights to reuse all their material in any form.</p>
    <p>If the proposal is accepted, at least one of the authors listed in the proposal is expected to attend VLDB 2025 and present the tutorial.</p>

