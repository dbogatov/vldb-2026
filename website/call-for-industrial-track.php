    <h1>VLDB 2025: Call for Contributions - Industrial, Applications, and Experience Track</h1>

    <p>The Industrial Track of VLDB 2025 will cover all aspects of innovative commercial or industrial-strength data management systems and solutions. We also welcome novel real-world applications of data management systems and experience in applying recent research advances to real-world problems. We require that at least one of the authors has a non-academic affiliation.</p>

    <h3>Submission Guidelines</h3>
    We accept full papers (up to 12 pages + references) and extended abstracts (up to 6 pages + references). The papers should be prepared using the VLDB 2025 paper formatting guidelines.
Submissions must be made electronically, in pdf format, at the conference submission site: <a href="https://cmt3.research.microsoft.com/PVLDBv19_2026"> https://cmt3.research.microsoft.com/PVLDBv19_2026 </a>. 
Submissions to the industrial track are required to include all author names and affiliations. Any changes to authorship after paper acceptance will require approval by the industrial track chairs. Authors are encouraged to contact the industrial track chairs if they need any clarification regarding the suitability of their work to this track.

<h3> Conflicts of Interest </h3>
It is the full responsibility of all authors of a paper to identify all and only the PC members with whom they have a conflict of interest (COI), as defined below. Papers with incorrect or incomplete COI information at the time when submission closes will be desk rejected.
A paper author has a conflict of interest with a PC member when, and only when, one or several of the following conditions holds:

  <ul>
<li>the PC member is a co-author of the paper;</li>
<li>the PC member has been a co-worker in the same organization within the past two years;</li>
<li>the PC member has been a collaborator within the past two years;</li>
<li>the PC member is or was the author’s primary thesis advisor, no matter how long ago;</li>
<li>the author is or was the PC member’s primary thesis advisor, no matter how long ago; or</li>
<li>the PC member is a relative or close personal friend of the author.</li>
 </ul>


    <h3>Important Dates (All deadlines below are 5pm PT.)</h3>
    <ul>
        <li>Submission deadline: March 3, 2025</li>
        <li>Notification: May 5, 2025</li>
        <li>Camera-ready copy due: June 16, 2025</li>
    </ul>

