    <h1>VLDB 2025: Information for Poster Presenters</h1>

<p>In-person poster presentations in Vancouver have been scheduled by the PC Chairs and are listed in the <a href='https://vldb.org/2025/?program-schedule-posters'>Poster Sessions</a> page

<p>We provide poster boards for poster presenters in the Pavilion Ballroom. These poster boards can fit two A0 posters (in portrait) on each side. These are double-sided boards with two presenters on each side (4 posters per poster board).</p>

<p>For those authors who told us they would like to have an in-person presentation of a poster, we assigned posters to a&nbsp;<a href="https://vldb.org/2025/?papers-research" style="color:blue" target="_blank"><span style="color:#3a84df">poster session</span></a>. Please check&nbsp;<a href="https://vldb.org/2025/?program-schedule-posters" style="color:blue" target="_blank"><span style="color:#3a84df">the Poster Sessions page</span></a>&nbsp;for your assigned session. The posters can be displayed on the day of the assigned session and must be removed at the end of your poster presentation by 19:30. Each poster will be assigned a number, which is used to identify its poster board. The number can be found both on&nbsp;the pages for&nbsp;<a href="https://vldb.org/2025/?program-schedule-posters" style="color:blue" target="_blank"><span style="color:#3a84df">poster sessions</span></a>&nbsp;and for&nbsp;</span></span><a href="https://vldb.org/2025/?program-schedule-posters" style="color:#1155cc" target="_blank">VLDB&nbsp;Journal</a>&nbsp;papers.</p>

Dimensions of the posters: Maximum poster size is A0 (0.84m x 1.19m) (33.1 in x 46.8 in) (portrait orientation only).
Note that A0 size posters in landscape mode will not fit.

Each poster presenter is responsible for printing, bringing their own poster and removing their poster at the end of their session.

