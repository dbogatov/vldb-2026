    <h1>VLDB 2025: Talk Recording Instructions</h1>

<h2>Recording Instructions</h2>
<p>Please note: We expect that all authors give their presentations live - either in-person here in Vancouver or via during the on-line event. The pre-recorded videos will be released after the conference if given consent.</p>

<p>To make sure that your presentation is able to be shown and of high quality, please make sure that you prepare a video that is in mp4 format. Failure to do so may result in your video not being shown.</p>

<p>Video duration must not exceed the following hard limits:</p>
<ul>
  <li>Research or industry papers: 15 minutes</li>
  <li>Demonstrations: 15 minutes</li>
</ul>

<p>We recommend you use the Zoom application (the desktop version, not the web version) to record your talk. You may follow the steps below:</p>

<ul>
  <li>Start a new meeting</li>
  <li>Enable your camera and microphone</li>
  <li>Start the presentation</li>
  <li>Share your screen</li>
  <li>Start recording</li>
</ul>

<p>The recordings will be saved locally as mp4 files when you end the meeting after stopping recording.</p>

<p>It is important that after recording, the video not be edited using anything less than a professional editing suite. Using other editing software, especially QuickTime, may result in corrupted files or audio and video not in sync. In any case, if you need to edit, make sure you enabled the Zoom setting 'Recording - optimise for 3rd party video editor'. Some tips for improving recording quality:</p>

<ul>
    <li>The number one complaint that we have seen about talks in past online conferences is poor audio quality. In order to allow the best quality possible, please:</li>

    <ul>
    <li>Consider using earphones and an external microphone rather than relying on laptop built-in options</li>
    <li>If you use a corded headphone with a mic, please make sure that the microphone does not brush against your clothes, since that causes audio disturbances</li>
     <li>Use an isolated room with no fish tanks or open windows</li>
     <li>Do a test recording to ensure your audio quality is good, with no disturbance/echoes, before you do the “real” recording
</li>
  </ul>
<li>Do a test recording to ensure your audio quality is good, with no disturbance/echoes, before you do the “real” recording
</li>


  <li>We also recommend you keep the picture-in-picture window that shows up when you enable your camera, so that viewers can see you as you speak. You can even make it a bit larger than the default to make the presentation more personal and engaging. You can move around the image if it is in the way of some slide content.</li>

  <li>Recommended Zoom settings:</li>
  <ul>
    <li>Under 'Video' enable 'HD'</li>
    <li>Under 'Audio', enable 'high-fidelity music mode' (optional as it increases CPU utilization)</li>
     <li>Under 'Recording', enable 'optimize for 3rd party video editor' and 'record video during screen sharing'</li>
  </ul>
</ul>

<h2>Tips for Creating Inclusive Talks and Videos (adapted from <a href='https://2021.sigmod.org/calls_papers_inclusion_and_diversity.shtml'>prior conferences</a>)</h2>
We now explain some additional tips on inclusive practices when creating slide content for talks, as well as during speaking and recording videos.

<h3>Slide content</h3>
It helps improve readability if your font sizes are not too small and if you avoid packing too much content onto one slide. If possible, avoid embedding text into images if similarly effective alternatives exist for rendering your content. Images in slides are usually unreadable for screen readers, which are often used by people with visual impairments.

<h3>During speaking/recording videos</h3>
It helps improve legibility if you pause for a moment in between sections and also during slide transitions. If possible, avoid speaking too fast, since that can make comprehension harder for many non-native English speakers, as well as people with hearing impairments. If you plan to record your face while speaking, look directly at the camera while speaking so that lipreading is more feasible for people with hearing impairments.


