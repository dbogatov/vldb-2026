    <h3>Submission Guidelines</h3>
    <p>PhD workshop submissions must be submitted electronically, in PDF format, using <a href="https://cmt3.research.microsoft.com/VLDBPhDW2025">the PhD Workshop CMT</a>. Submissions must be single-author and any supervisors must be explicitly identified with "supervised by" on the paper and below the author's name.</p>
    <p>Papers must be submitted in the two-column <a href="https://www.overleaf.com/latex/templates/template-for-submissions-to-ceur-workshop-proceedings-ceur-ws-dot-org/wqyfdgftmcfw">CEUR-ART format</a> and be limited to 4 pages, inclusive of ALL material. It is acceptable if specific portions of the thesis work have been published or submitted for publication; being limited to 4 pages, this does not constitute a duplicate submission to VLDB.</p>

    <h3>Review Process</h3>
    <p>The review and decision of acceptance will balance many factors; these include the quality of your proposal and where you are within your doctoral education program. They also include external factors, such as ensuring that, as a group, the accepted candidates exhibit a diversity of backgrounds and topics.</p>
    <p>Candidates who have a clearly developed idea, who are formally considered by their institution to be working on their dissertation, and who still have time to be influenced by participation in the PhD Workshop will receive the strongest consideration.</p>

