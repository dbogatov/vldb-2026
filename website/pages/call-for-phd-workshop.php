    <h1>VLDB 2025: Call for Contributions - PhD Workshop</h1>

    <p>VLDB PhD Workshops are a unique opportunity for graduate students to present and discuss their research work in the context of a premier international conference. The workshop provides a forum that facilitates interactions among PhD students and stimulates feedback from more experienced researchers.</p>
    <p>VLDB 2025 welcomes submissions from PhD students at any stage of their PhD work. If you are in the early stages of your studies, the submission should clearly describe the problem focused on, explain why it is important, detail why the existing solutions are not sufficient, and give an outline of the new solutions that are pursued. If you are in the middle or close to completion, the submission should be more concrete in describing your contribution, but still in the context of the doctoral work.</p>

