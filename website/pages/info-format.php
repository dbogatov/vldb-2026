<h1>Conference Format</h1>
<p>To have the best experience for all the attendees and facilitate active discussions and collaborations between presenters and attendees, the 2025 VLDB Conference and all co-located workshops are geared towards 100% in-person attendance at the conference venue.</p>

<p>There will be NO live streaming or remote participation during the in-person sessions of the conference and the workshops.
There will be a separate on-line only event on Friday, September 1, 2025 - however, it is reserved for presentations by authors who, for valid reasons, are unable to present in-person. Details are below.</p>

<h2>RESEARCH AND INDUSTRY PAPERS – On-Line Only Event</h2>
<ol>
  <li>If a paper cannot have an in-person presenter, the authors need to provide a valid justification, e.g., travel restrictions from their government or visa application not granted for even one author etc. </li>
  <li>If a paper cannot have an in-person presenter (due to a valid justification), the authors must present their paper in the on-line only event on Friday September 1, 2025.</li>
  <li>The on-line only event is separated from the main conference and will take place on Friday September 1 according to the following time-slots: </li>
  <ul>
    <li>Morning 7am -10am (Canada Pacific Time)</li>
  <li>Late afternoon 4pm-10pm (Canada Pacific Time)</li></ul>
  <li>Authors that require to present their paper on-line MUST inform the conference organizers by filling in this form <a href="https://docs.google.com/forms/d/e/1FAIpQLSecXuDoC50PUTKVTTIDn1YoXRxBfEru0GegIVb6G_628fV77g/viewform">click here</a> no later than August 15th, 2025, and indicate the preferred time-slot.</li>
  <li>NOTE: Even if a paper is presented online, the paper must still be covered by a regular in-person registration.</li>
  
</ol>

<h2>DEMOS – On-Line Only Event</h2>

<ol>
  <li>If a demo cannot have an in-person presenter, the authors need to provide a valid justification, e.g., travel restrictions from their government or visa application not granted for even one author etc.</li>
  <li>If a demo cannot have an in-person presenter (due to a valid justification), the authors must present their demo in the on-line only event on Friday September 1, 2025.</li>
  <li>The on-line only event is separated from the main conference and will take place on Friday September 1 according to the following time-slots: </li>

  <ul>
    <li>Morning 7am -10am (Canada Pacific Time)</li>
  <li>Late afternoon 4pm-10pm (Canada Pacific Time)</li></ul>
  <li>Authors that require to present their paper on-line MUST inform the conference organizers by filling in this form <a href="https://docs.google.com/forms/d/e/1FAIpQLSecXuDoC50PUTKVTTIDn1YoXRxBfEru0GegIVb6G_628fV77g/viewform">click here</a> no later than August 15th, 2025, and indicate the preferred time-slot.</li>
  <li>NOTE: Demos given in the on-line only event will be excluded for consideration for the best demo award.</li>
  
</ol>

