<h1>VLDB 2025: Industry Challenge</h1>

Cloud computing has revolutionized the way we approach computing. The database community has made significant contributions to the development of "Cloud Native" data services that take advantage of the unique capabilities of infrastructure clouds, including elastic resource allocation, database system optimization, and hardware and software co-optimization. Additionally, the deployment of Large Language Models has introduced new features to database services, such as Natural Language to SQL (NL2SQL), which enables easier access to databases for everyone. 

<br><br>

To address the technical challenges that come with this industry trend, our workshop will be organized in a round-table format. Each session will begin with a presentation of research problems in industry practice, followed by an open discussion on possible directions to tackle them. Our workshop aims to create a platform for researchers and practitioners to exchange ideas and work in progress, without requiring paper submission or a full presentation. The goal is to define research scope together and guide the research community in effectively addressing these emerging challenges. 

