    <h1>VLDB 2025: Call for Contributions - Panels</h1>

    <p>We solicit proposals for panels at the VLDB 2025 Conference.
Panels should be provocative, informative, and entertaining; and they are expected to discuss new, exciting, or controversial issues of broad interest to the data management and data science and engineering communities. Proposal evaluation criteria include relevance to VLDB, qualification of the panelists, perceived interest in the topic, diversity and inclusion considerations, and long-term vision.
</p>

Panel proposals must include a description of the panel topic (no more than 1.5 pages) and address the following points:
<ul>
<li>Why is this topic of broader interest, exciting, or controversial?</li>
<li>What are possible controversies that might spark a discussion?</li>
<li>What are the likely positions of the panelists on controversial topics?</li>
<li>What questions or prompts are given to the panelists to prepare their opening statements?</li>
<li>What questions will the moderator use to get the discussion going?
<li>Name, affiliation, brief bio, and contact information for the panel chair/moderator</li>
<li>Names, affiliations, brief bios, and rationale for inclusion in the panel for up to 6 panelists. </li>
</ul>

The proposed panelists must have made a commitment to participate.
Proposals should be submitted to the panel chairs via email:

