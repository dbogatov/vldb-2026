<h1><span>VLDB 2025: Call for Contributions - Workshops</span></h1>
<h3><span>Information</span></h3>
<p><span>VLDB 2025 invites submissions for workshop proposals on all topics covered by the conference. Workshop proposals that advance the field of data management in new directions are especially encouraged. VLDB workshops may be half-day or full-day events.</span></p>
<p><span>All VLDB 2025 workshops will be "serviced", meaning that logistics such as registration, catering, room allocation, and audio-visual/streaming arrangements will be handled by the VLDB conference organizers. The workshop organizers are responsible for defining the technical program and workshop format.</span></p>
<h3><span>Important Dates (All deadlines below are 5pm PT.)</span></h3>
<ul>
  <li><span>Submission Deadline: <b>January 17th, 2025</b></span></li>
  <li><span>Notification: <b>January 31st, 2025</b></span></li>
</ul>
<h3><span>Submission Guidelines</span></h3>
<p><span>Workshop proposals should be submitted via e-mail to VLDB 2025 workshops co-chairs, John Paparrizos and Norman Paton with "VLDB 2025 Workshop Proposal" as the e-mail subject. Workshop organizers with special requirements should contact the workshop co-chairs as soon as possible; such requests will be considered on a case-by-case basis.</span></p>
<p><span>Proposals must be in pdf, not exceeding </span><b>4</b><span> pages, and including the following (in the order given):</span></p>
<ul>
  <li><span>workshop title</span></li>
  <li><span>chairs and organizers: for each providing name, affiliation, email, and short bio (including previous experience in organizing workshops or conferences), with one of them indicated as the primary contact</span></li>
  <li><span>a description of the workshop's topic and its goals</span></li>
  <li><span>a statement motivating the topic and approach of the workshop</span></li>
  <li><span>a statement on how the workshop complements or relates to other events</span></li>
  <li><span>desired workshop format, including preferred date and duration</span></li>
  <li><span>a description of the submission review process, including key dates and coverage of how conflicts of interest are handled</span></li>
  <li><span>plans for publicity</span></li>
  <li><span>potential (or accepted) program committee members</span></li>
  <li><span>potential (or accepted) speakers or keynotes</span></li>
  <li><span>historical information about the most recent previous editions of the workshop (if this is not its first edition), including:</span></li>
  <ul>
  <li><span>date, venue, and affiliated event</span></li>
  <li><span>number of submitted and accepted papers</span></li>
  <li><span>number of participants</span></li>
  </ul>
</ul>

<p><span>VLDB will provide a means of hosting and disseminating papers from workshops. If you do not want to use this, please also include a plan for the proceedings.</span></p>
<h3><span>Additional Information</span></h3>
<p><span>Accepted workshops will be scheduled based on the preferred date and duration, the workshop topics, and the number of available slots. We expect that VLDB 2025 will be able to accommodate up to four parallel workshops on each of the two workshop days.</span></p>
<p><span>The organizers of accepted workshops are required to announce the workshop and call for papers, solicit submissions, conduct the review process, compose a workshop program, </span><span>and </span><span>prepare submissions for publication</span><span>. Workshop programs must comply with a shared catering schedule,</span><span> and workshop programs must be published 15 days prior to the early registration deadline</span><span>.</span></p>
<p><span>Proposal evaluation criteria include relevance to VLDB, scientific quality, quality of proposed organization, evidence of interest (e.g., previous submission and attendance counts), and overlap with other proposals.</span></p>
<p><span>Note that VLDB 2025 is an in-person event. Workshop chairs should be physically present at VLDB 2025. The workshops will be held on the first and last days of the conference, so 1st and 5th September 2025.</span></p>
<h3><span>Workshop Program Chairs</span></h3>
<p><span>John Paparrizos, Ohio State University (paparrizos.1@osu.edu)</span></p>
<p><span>Norman Paton, University of Manchester (norman.paton@manchester.ac.uk)</span></p>

