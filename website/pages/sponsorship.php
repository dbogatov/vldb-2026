            <h1>VLDB 2025: Sponsorship</h1>

            <h3>Who are we?</h3>
            <p>
              VLDB, started in 1975, is the premier database conference. It is an international
              conference held annually. Different countries ranging from China to India, Germany,
              Brazil and USA have hosted VLDB.  It has introduced numerous influential concepts such
              as "Big Data", "Data Science", and "Cloud" before these names became widely used. Some
              of the seminal and highly cited research papers in these topics were published in our
              conference. For almost half a century and after an explosive growth in the computing
              industry, we remain the home for the latest cutting-edge research and industrial
              innovation in the data arena. Even today, the technology and research presented at our
              conference is typically 5 to 10 years ahead of its time. VLDB 2025 will be held in
              London in the United Kingdom, on September 1-5, 2025.
            </p>

