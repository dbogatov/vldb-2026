            <h2 id="es">VLDB Endowment Sponsorship</h2>
            <p>
              VLDB invites companies wishing to sponsor the VLDB Conference to become Endowment Sponsors. The newly
              introduced Endowment Sponsorship hopes to foster increased collaboration and dialogue between the
              sponsoring companies and the VLDB research community by providing enhanced benefits to VLDB’s corporate
              partners. Endowment sponsorships are intended for companies/organizations with a longer-term interest
              in database research and development who wish to stay connected with the VLDB community. The expectation
              is that such companies would stay engaged with and sponsor VLDB over multiple years and would benefit
              from the increased level of benefits that such a sponsorship will provide. Endowment sponsors at a given
              level (e.g., Platinum) are automatically conference sponsors at the same level for the given year. In
              addition to all the benefits that a VLDB conference sponsorship offers at the given level, the Endowment
              sponsors will further receive the following benefits:
            </p>

            <ol>
              <li>
                Recognition as a VLDB Endowment sponsor at all VLDB events year round: The endowment sponsor, in
                addition to be recognized as sponsors (at the appropriate level of support sponsor-- e.g., Gold,
                Platinum, Diamond) during the conference, would also be recognized at other VLDB events such as summer
                schools, sponsored workshops, etc.
              </li>
              <li>
                Ambassador Service: The VLDB endowment will identify a specific individual from the VLDB community
                who will serve as a liaison to ensure that the endowment sponsors are able to derive maximum value from
                the VLDB community sponsorship all year round.
              </li>
              <li>
                Opportunity to participate in an online advisory meeting twice a year. Endowment sponsors will have an
                opportunity to participate in a bi-annual advisory meeting. During the meeting the sponsor
                representatives will have an opportunity to engage with the Endowment representatives to provide
                collective feedback on activities &amp; sponsorship benefits, as well as to share their vision(s) for the
                growth of the field.
              </li>
              <li>
                Access to a curated list of graduating students looking for jobs not just during the conference, but
                every quarter. The names will consist of students who opt-in and would like VLDB to distribute their
                name to potential employers.
              </li>
              <li>
                Access to Leading Database researchers on topics of mutual interest: The VLDB Endowment (through the
                organization’s liaison - see benefit 2 above) will enable sponsors to connect with experts on specific
                topics of interest to their sponsoring organization. Typically, these experts will be ones who have
                offered tutorials / lectures / keynotes at the conference and/or explicitly expressed interest in
                donating a few hours of their time connecting with sponsoring companies. The experts will offer
                consulting / talks on specific topics of mutual interest exclusively for the Company’s internal audience.
                The goal of this benefit is to seed potential opportunities for research collaborations / consulting
                between academics and corporate partners. The Endowment will provide Endowment sponsors with a list of
                researchers and topics to choose from. Different levels of sponsorship will provide different levels of
                access to researchers: Gold – one 2-hour session, Platinum – two 2-hour sessions, and Diamond – three
                2-hour sessions. Please note that this benefit is based on the VLDB researchers volunteering to contribute
                their time to companies; the list of available topics and researchers may change dynamically.
              </li>
            </ol>

