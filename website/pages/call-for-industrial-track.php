    <p>The Industrial Track of VLDB 2025 will cover all aspects of innovative commercial and industrial-strength (including open source) data management systems and solutions. We also welcome submissions describing novel applications of data management systems and experience in doing so for challenging industry problems. We require that at least one of the authors has a non-academic affiliation.</p>

    <h3>Submission Guidelines</h3>
    <p>The submissions to this track may be up to 12 pages long (excluding references). The papers should be prepared using the VLDB 2025 paper formatting guidelines as specified at <a href="https://www.vldb.org/pvldb/volumes/18/formatting">https://www.vldb.org/pvldb/volumes/18/formatting</a>.
Submissions to the industrial track are required to include all author names and affiliations. After a paper is accepted, the set of authors cannot be changed.</p>

<p>Submissions must be made electronically, in pdf format, to the industrial track at the conference submission site: <a href="https://cmt3.research.microsoft.com/PVLDBv19_2026">https://cmt3.research.microsoft.com/PVLDBv19_2026</a>. It will be open for submission to the industrial track shortly.
</p>

<h3>Revision Phase</h3>
The Program Committee expects the initial submission itself to be of quality high enough to be accepted. Unlike the research track, the revision phase in the industrial track will be akin to what is traditionally viewed as shepherding with requests for small changes only. This is why there is only a 2 week interval between the initial notification and the final decision.

<h3> Conflicts of Interest</h3>
<p>
To minimize biases in the evaluation process, we use CMT's conflict management system, through which authors should flag conflicts with members of the Industrial Track Program Committee. The Industrial Track follows the same rules as the VLDB 2025 Research tracks, described at <a href="https://www.vldb.org/pvldb/volumes/18/submission">https://www.vldb.org/pvldb/volumes/18/submission</a>. They are:
</p>

<p>
Each author is responsible for entering their own domain and individual PC conflicts on CMT. All authors of a paper (listed in the pdf) must register themselves in CMT and declare their individual domain and PC conflicts at the time of submission. It is the full responsibility of all authors of a paper to identify and declare all CoIs with members of the PC prior to the submission deadline. Conflicts of interest will also be checked using an automated CoI detection tool. Submissions with undeclared conflicts or spurious conflicts will be desk-rejected.
</p>

<p>
You can mark your domain and individual PC conflicts by clicking on your name (upper right-hand side on CMT) and selecting "Personal Conflicts". An author's declared conflicts will be automatically applied to all of their submissions.
</p>
<p>
X and Y have a conflict of interest (CoI) if any of the following applies:
</p>
  <ul>
<li>X and Y have worked in the same university or company in the past 2 years, or will be doing so in the next 6 months on account of an accepted job offer. Different campuses within the same university system do not count as the same university for this purpose (e.g., UC Berkeley does not have a conflict with UC Santa Barbara).</li>
<li>X has been a co-author of a paper with Y in the last 3 years, or of 4 (or more) papers in the last 10 years. (Note that we consider the publication year and not the submission year or the month of publication (i.e., we do not distinguish at month-level granularity). For example, in 2024, the last 3 years start from January 2021 of the paper's publication date.)</li>
<li>X has been a collaborator within the past 2 years, as evidenced in a joint publication (subsumed by the stricter rule on co-authorship above), joint research project, or co-organizing events (e.g., co-chairs of conferences), or are collaborating now (including co-authorship on papers not resulted in final publication yet).</li>
<li>X is the PhD thesis advisor of Y or vice versa, irrespective of how long ago this was.</li>
<li>X is a relative or close personal friend of Y.</li>
 </ul>


