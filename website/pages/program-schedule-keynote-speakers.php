            <p class="schedule-item schedule-item-has-photo">
                <img class="schedule-photo" src="https://vldb.org/2025/img/feifei-li.jpg">
                <strong class="schedule-item-title">Modernization of Databases in the Cloud Era: Building Databases that Run Like Legos</strong>
                <span class="schedule-item-people">
                    <span class="author">Dr. Feifei Li</span> Vice President, Alibaba Group; Director of Alibaba Cloud Database Product Business Unit; Director of DAMO Academy Database Lab
                </span>
                
                <span class="schedule-item-abstract">
                    <a class="schedule-item-action"></a>
                    <strong class="schedule-part-label">Abstract:</strong>
                   Utilizing the cloud for common and critical computing infrastructures has already become the norm across the board. The rapid evolvement of the underlying cloud infrastructure and the revolutionary development of AI present both challenges and opportunities for building new database architectures and systems. It is crucial to modernize database systems in the cloud era, so that next-generation cloud-native databases may run like legos–they are adaptive, flexible, reliable, and smart towards dynamic workloads and varying requirements. That said, we observe four critical trends and requirements for the modernization of cloud databases: embracing cloud-native architecture, cloud platform integration/orchestration, co-design for data fabric, and AI-first/AI-inside. Modernizing database systems by adopting these critical trends and addressing key challenges associated with them provides ample opportunities for data management communities from both academia and industry to explore. We will provide an in-depth case study of how we modernize PolarDB with respect to embracing these four trends in the cloud era. Our ultimate goal is to build databases that run just like playing with Legos so that a database system fits rich and dynamic workloads/requirements in a self-adaptive, performant, easy-to/intuitive-to-use, reliable, and intelligent manner.
                </span>

                <span class="schedule-item-bio">
                    <a class="schedule-item-action"></a>
                    <strong class="schedule-part-label">Bio:</strong>
                    Feifei Li is currently a Vice President of Alibaba Group, director of the database product business unit of Alibaba Cloud, and director of the database lab of DAMO Academy. He was a full professor at the School of Computing, University of Utah before joining Alibaba in 2018. He has won multiple awards from ACM/IEEE and others. He is a recipient of ACM SIGMOD 2025 best paper award, EDBT 2022 10 Years Test of Time Award, IEEE ICDCS 2020 best paper award, ACM SoCC 2019 Best Paper Award Runner-up, IEEE ICDE 2014 10 Years Most Influential Paper Award, ACM SIGMOD 2016 Best Paper Award, ACM SIGMOD 2015 Best System Demonstration Award, and IEEE ICDE 2004 Best Paper Award. He has been an editor, PC co-chair, and core committee member for many prestigious journals, conferences, and technical meetings.  He has led the R&D efforts of building cloud-native database systems and products at Alibaba, such as cloud-native database PolarDB and cloud-native data warehouse AnalyticDB which propels Alibaba Cloud Database to be named a Cloud DBMS market leader by Gartner three years in a row. He is an ACM/CCF/IEEE Fellow.
                </span>
