    <h1>Travel Support</h1>
The long-term goal of the program is to encourage talented young scientists to pursue a career in database research. The grants are funded by:
The National Science Foundation (NSF) for students enrolled in a US university. The grants of up to US$1,750 cover travel and lodging as well as registration expenses for in-person attendees. Successful applicants of the NSF-funded travel support are obliged to use US-flag airline carriers for traveling and will be required to deliver a short written report after the conference.
The SPEND committee from the VLDB Endowment for non-US  based students. The grants of up to US$1,500 cover travel and lodging expenses for in-person attendees. Registration fees are waived for awardees. Preference will be given to applicants who demonstrate keen interest in database research and are from regions that are under-represented in the database community.
Recipients of the travel support are responsible for their travel arrangements, including health insurance, if necessary. While the free registration amount will be provided by a voucher code at registration time, the amount for the travel and lodging costs of the in-person attendees will be paid in the form of a reimbursement either during or after the conference.

<h2>Eligibility and Selection Criteria</h2>
Applicants must be full-time students in good standing (graduate or undergraduate), or postdoctoral fellows/researchers who are either
affiliated with universities that do not already have considerable presence at VLDB conferences,
or
have a paper at the conference (or associated events).
Applicants for travel support must explicitly state that they do not have any other funding to attend. Presenting a paper accepted at the main conference or the workshops in VLDB 2025 is welcome but not mandatory.


<h2>Applications</h2>


<p>Applicants should fill in the application form below by June 18th, 2025. Applicants must also have a nominator (faculty advisor for student applicants, or senior faculty member or department head for postdoc and research assistant applicants), who must fill in the nominator form.</p>

<p><strong>Application form URL</strong>: <a href="https://docs.google.com/forms/d/1wwaXkYsG_QTW_aSseCaEV1sv1YwKtxBfKATgndq7FO4/viewform?edit_requested=true">click here</a></p>
<p><strong>Nominator form URL</strong>: <a href="https://docs.google.com/forms/d/1GKfsiFMD2PdXDw8ZzuQQyATOc084XFT7Jkp5TFy3b6o/edit?ts=6458d7b9">click here</a></p>



<p>Additionally, nominators are kindly asked to send an extra email from their official email-account to felix.naumann@hpi.de before June 18th, 2025 with the subject line of the email being "VLDB 2025 Attendance Support Nomination for ..." in which they confirm their nomination of the applicant. </p>



<p>Should you have any problems with opening the links or filling in the forms, please contact us via felix.naumann@hpi.de. Please note: Applications of US students will be automatically assessed also for the NSF-funded travel grant. US-students do not need to follow a separate application process.</p>



<p>Selected candidates will be informed by June 25th, 2025. Attendees applying for the travel support are advised to not register before hearing from us, since the travel support can include free registration; note that the early registration deadline is July 5th, 2025.</p>


