    <h1>VLDB 2025: Call for Contributions - Workshops</h1>

    <p>VLDB 2025 invites submissions for workshop proposals on all topics covered by the conference. Workshop proposals that advance the field of data management to new directions are specially encouraged. VLDB workshops may be half-day or full-day events.</p>
    <p>All VLDB 2025 workshops will be "serviced," meaning that logistics such as registration, catering, room allocation, and audio-visual arrangements will be handled by the VLDB conference organizers. The workshop organizers are responsible for defining the technical program and workshop format.</p>

