    <h3>Demo Proposals</h3>
    <p>The proposal must describe the demonstrated system, and state the novelty and significance of the contribution to data management research, technologies, and/or its applications. The proposal should pay special attention to describing the exact demonstration scenarios for the given system. This should include how the audience will experience the demo, what kind of functionality is supported, user scenarios, interface and interaction options, etc. Proposals must be submitted in <a href="./?formatting-guidelines">camera-ready format</a> and limited to 4 pages, inclusive of ALL material.</p>

    <h3>Video Submissions</h3>
    <p>We specifically encourage the submission of a demonstration video (of up to 5 minutes, 50MB max. file size) together with your demonstration proposal via <a href="https://cmt3.research.microsoft.com/PVLDBv19_2026/">CMT</a>. Both the demonstration proposal and the video will then be accessible by the reviewers. Your video should summarize your demonstration and also audio-visually highlight its most important aspects, such as the user interface, options for user interactions, the system setup, etc. The video should be submitted in MPEG/AVI/MP4 format and be playable by the common media players. Please note that you will need to first finish your demo proposal submission and then edit it to add the video as a supplementary file.</p>

    <h3>Conflicts</h3>
    <p>To minimize biases in the evaluation process, we use <a href="https://cmt3.research.microsoft.com/PVLDBv19_2026/">CMT</a>'s conflict management system, through which authors should flag conflicts with the Demo Program Committee members. All authors of a submission must declare conflicts on <a href="https://cmt3.research.microsoft.com/PVLDBv19_2026/">CMT</a> prior to the submission deadline.</p>
    <p>You have a conflict with X:</p>
    <ul>
        <li>If you and X have worked in the same university or company in the past two years, or will be doing so in the next six months on account of an accepted job offer. Different campuses do not count as the same university for this purpose -- UC Berkeley does not have a conflict with UC Santa Barbara.</li>
        <li>X has been a co-author of a paper with Y in the last 3 years, or of 4 (or more) papers in the last 10 years.</li>
        <li>X has been a collaborator within the past two years, as evidenced in a joint publication (subsumed by the stricter rule on co-authorship above), joint research project, or jointly organized event, or are collaborating now.</li>
        <li>If you are the PhD thesis advisor of X or vice versa, irrespective of how long ago this was.</li>
        <li>If X is a relative or close personal friend.</li>
    </ul>
    <p><strong>Submissions with undeclared conflicts or spurious conflicts will be DESK REJECTED. There will be NO exceptions to this rule.</strong></p>

    <h3>Originality and Duplicate Submissions</h3>
    <p>Note that demonstration proposals must not have been published, or be under consideration for publication, at any other forum. Demonstration proposals should specifically focus on the genuine aspects of the described systems and the intended interaction with the audience; they should not be a short version of an existing conference paper (whether or not this may have been published elsewhere).</p>

    <h3>Demo Submission</h3>
    <p>Demonstration proposals must be submitted electronically, in PDF format, using <a href="https://cmt3.research.microsoft.com/PVLDBv19_2026/">CMT</a>. When creating a new paper submission, you will be given the option to choose a track. Choose the "Demonstrations" track for your demo proposal. A respective option to upload the demonstration video will be made available.</p>

