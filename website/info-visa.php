    <h1>VLDB 2025: Visa Information</h1>

<h2>Visa</h2>
Please refer to the Immigration, Refugee and Citizenship Canada <a href="https://www.canada.ca/en/immigration-refugees-citizenship/services/visit-canada/about-visitor-visa.html">(IRCC) website</a> to learn about a
temporary resident visa (visitor visa) and the process to <a href="https://www.canada.ca/en/immigration-refugees-citizenship/services/visit-canada/apply-visitor-visa.html">apply for one</a>a>.
The applicant must meet all the <a href="https://www.canada.ca/en/immigration-refugees-citizenship/services/visit-canada/eligibility.html">eligibility criteria</a> in order to be granted a visa. Please note that visa
applications are considered on a case-by-case basis based on the information presented by the applicant.
The onus is on the applicant to show that they meet the requirements for a temporary resident visa.
While a special event code has been assigned to VLDB 2025, it does not offer any advantage to the
applicant, in terms of a positive visa outcome.
Participants are encouraged to check the <a href="https://www.canada.ca/en/immigration-refugees-citizenship/services/application/check-processing-times.html">processing times calculator</a> for guidance, but generally it is
recommended to apply at least 12 weeks in advance of departure date to ensure that the registrant
receives the visa in time for the start of the event. Processing times for visa applications vary depending
on the visa office and the time of the year.

<h2>Letters of Invitation</h2>
Invitation letters for visa purposes will be issued to registered and paid delegates. Registration will be
open in April 2025. Request can be made directly on the registration portal where you can indicate your
passport details. Please allow us 2-3 business days after payment to process your request.
Invited speakers may request a visa letter before registering for the conference in exceptional cases.
Please send us your request at (VLDB-Info@mci-agency.com).
Kindly note that an invitation letter for visa purposes does not imply any obligation, financial or
otherwise, by VLDB 2025.
